# Crie um programa que mostre na tela todos os números pares
# que estão no intervalo entre 1 e 50
inicio = 1
fim = 50
intervalo = 2

for c in range(inicio+1, fim, intervalo):
    print(c)
print('Este são todos os números pares de 1 a 50')